	<p>XpressEngine은 위의 구조를 기반으로 Core를 통해 개별 프로그램과 스킨을 실행하여 결과물을 생성합니다. 다양한 개성을 가지고 만들어진 프로그램, 스킨의 조합을 통해 다채로운 기능과 다양한 디자인을 가진 웹 사이트를 만들 수 있습니다.</p>


