	<h1>About XpressEngine</h1>

	<p><em>XpressEngine = eXpress(표현하다) + press(발행하다) + Engine(기관, 장치)</em> XpressEngine은 누구나 쉽고 편하고 자유롭게 콘텐츠를 발행을 할 수 있도록 하기 위한 CMS(Content Management System)입니다. 급변하는 인터넷 환경과 다양한 기능의 요구에 부응하기 위해 XpressEngine은 다음과 같은 특징을 가지고 있습니다.</p>

	<h2>확장형 구조</h2>

	<p>웹사이트를 구성하는 요소들은 무척 다양합니다.<br>콘텐츠를 작성하는 WYSIWYG 에디터부터 작성한 콘텐츠를 여러 형태로 보여주는 게시판, 위키, 블로그 등과 같은 프로그램이 있으며 더욱 체계적인 웹사이트 관리를 위한 회원 관리 기능 등 웹 사이트에는 많은 구성 요소들이 필요합니다. 또한, 이러한 기능들은 대부분 HTML/CSS로 표현하는데 사이트의 디자인이나 사용자의 필요에 따라 표현 양식을 달리 해야 할 때도 있습니다.<br>XpressEngine은 다양한 기능과 디자인에 대한 사용자들의 요구를 충족시키기 위해 각각의 기능과 디자인이 구조적으로 연결되는 모듈형 구조를 사용합니다.<br>이때, 모듈, 애드온, 위젯 등의 요소에서 기능을 담당하며 디자인은 스킨을 통해 변화시킵니다.</p>

